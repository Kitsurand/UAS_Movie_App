class Showtime {
  final String id;
  final String movieId;
  final String cinemaId;
  final String date;
  final String time;
  final String screenName;
  final double pricePerSeat;

  const Showtime({
    required this.id,
    required this.movieId,
    required this.cinemaId,
    required this.date,
    required this.time,
    required this.screenName,
    required this.pricePerSeat,
  });
}
